# Мемоизация создаёт кэш при первом запуске, после этого мы просто берём из этого кэша значение,
# а значит только первый запуск первой функции будет сравним по длительности с первым запуском второй,
# а значит замеры будут точны только на одном повторе, или же на опустошении кэша каждый раз,
# но тогда пропадает смысл кэша. Поэтому мемоизация в данном случае только дезинформирует.