companny_money = {'nike': 12000, 'adidas': 15000, 'apple': 20000, 'sumsung': 18000, 'ozon': 10000, 'marvel': 23000}


# Сложность: O(n^2) квадратичная сложность (так как линейная функция вложена в линейноую)
def max_money(companny):
    a = sorted(companny.values())[:2:-1]  # O(n log n)
    result = {}  # O(k)
    for i in a:  # O(n)
        for key, val in companny.items():  # O(n)
            if i == val:  # O(n)
                result[key] = val  # O(1)
    return result


print(max_money(companny_money))

