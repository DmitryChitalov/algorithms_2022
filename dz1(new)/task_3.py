import heapq


# Сложность: O(n log n) линейно-логорифмическая сложность
def max_mooney_func(money):
    result = {}  # O(k)
    companny = heapq.nlargest(3, money, key=money.get)  # O(n log n)
    for key, val in money.items():  # O(n)
        if key in companny:  # O(1)
            result[key] = val  # O(1)
    return result


print(max_mooney_func(companny_money))

