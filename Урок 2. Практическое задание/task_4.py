"""
Задание 4.	Найти сумму n элементов следующего ряда чисел:
1 -0.5 0.25 -0.125 ...
Количество элементов (n) вводится с клавиатуры.

Пример:
Введите количество элементов: 3
Количество элементов - 3, их сумма - 0.75

Решите через рекурсию. В задании нельзя применять циклы.
Нужно обойтисть без создания массива!
"""


def row_sum(n: int) -> float:
    try:
        if n == 1:
            return 1
        else:
            return row_sum(n - 1) + (-0.5) ** (n - 1)
    except RecursionError:
        print('Введите положительное число отличное от нуля  и не более 52!')
        return exit(1)


n = int(input('Введите количество элементов: '))

print(f'Количество элементов - {n}, их сумма -', row_sum(n))

# n = 3
# row_sum(3)
# row_sum(2) -> 0.125
# row_sum(1) -> 0.125 + (-0.5)
# Срабатывает базовое условие n == 1 -> 0.125 + (-0.5) + 1 = 0.75
# Если ввести n <= 0 срабатывает исключение и программа завершается с кодом 1

