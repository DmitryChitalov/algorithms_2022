@time_counter
def get_list_el(lst, n):
    for i in lst:  # О(n) - линейная
        if i == n:  # О(n) - константная
            return lst[i]  # О(n) - константная


@time_counter
def get_dict_el(dct, n):
    for key in dct:  # O(n) - линейная
        if key == n:  # О(1) - константная
            return dct[key]  # O(1) - константная


