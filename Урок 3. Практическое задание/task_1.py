# С. Удаление элемента

@countTime
def popElList():
    for i in range(len(mas)):
        mas.pop()
    return


@countTime
def popElDic():
    for i in range(len(diction)):
        diction.popitem()
    return


print("\nУдалание элемента")
popElList()
