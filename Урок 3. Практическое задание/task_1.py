# (C) удаление элемента списка/словаря О(n)

@time_counter
def pop_list_el(lst=[]):
    for i in range(len(lst)):  # O(n) - линейная
        lst.pop()  # O(1) - константная


@time_counter
def pop_dict_el(dct={}):
    for i in range(len(dct)):  # O(n) - линейная
        del dct[i]  # O(1) - константная


